import type { Food, RouletteGroup, RouletteGroupFood } from '@/types/catalog';

function createFood(
  id: string,
  food: Omit<Food, 'id' | 'assetKey'>,
): Food {
  return {
    id: `fallback-food-${id}`,
    assetKey: null,
    ...food,
  };
}

function rouletteFood(food: Food, weight: number): RouletteGroupFood {
  return { food, weight };
}

